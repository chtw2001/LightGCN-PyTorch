    # Dropout 된 희소 행렬 반환
    def __dropout_x(self, x, keep_prob):
        size = x.size()
        index = x.indices().t()
        values = x.values()
        random_index = torch.rand(len(values)) + keep_prob
        random_index = random_index.int().bool()
        index = index[random_index]
        values = values[random_index]/keep_prob
        g = torch.sparse.FloatTensor(index.t(), values, size)
        return g
    
    def __dropout(self, keep_prob):
        if self.A_split:
            graph = []
            # self.Graph는 sparse matrix인데, 이게 실행이 될까?
            for g in self.Graph:
                graph.append(self.__dropout_x(g, keep_prob))
        else:
            graph = self.__dropout_x(self.Graph, keep_prob)
        return graph
    
    def computer(self):
        """
        propagate methods for lightGCN
        """       
        users_emb = self.embedding_user.weight
        items_emb = self.embedding_item.weight
        all_emb = torch.cat([users_emb, items_emb])
        #   torch.split(all_emb , [self.num_users, self.num_items])
        embs = [all_emb]
        if self.config['dropout']:
            if self.training:
                print("droping")
                # mask 된 희소 행렬
                g_droped = self.__dropout(self.keep_prob)
            else:
                g_droped = self.Graph        
        else:
            g_droped = self.Graph    
        
        for layer in range(self.n_layers):
            # A_split이 갖는 의미는? 메모리가 작아서 희소 행렬 나누어 계산하는것인가?
            if self.A_split:
                temp_emb = []
                for f in range(len(g_droped)):
                    temp_emb.append(torch.sparse.mm(g_droped[f], all_emb))
                side_emb = torch.cat(temp_emb, dim=0)
                all_emb = side_emb
            else:
                # embedding 가중치와 user-item행렬간 연관도 측정
                # ((user_n + item_n), emb_size)
                all_emb = torch.sparse.mm(g_droped, all_emb)
            embs.append(all_emb)
        # embs -> (원본 그래프와 embedding 가중치의 곱)*3
        # 동일한 그래프가 3개 들어있음
        # ((user_n + item_n), 3, emb_size)
        #   [[emb],  layer1의 임베딩
        #    [emb],  layer2 
        #    [emb]]  layer3
        # 이게 user_n+item_n개 만큼
        embs = torch.stack(embs, dim=1)
        #print(embs.size())
        # 3개 layer값 모두 더하기
        # ((user_n + item_n), emb_size)
        light_out = torch.mean(embs, dim=1)
        users, items = torch.split(light_out, [self.num_users, self.num_items])
        return users, items
    
    # 학습된 embedding을 기반으로 test rating
    def getUsersRating(self, users):
        # 추론 결과 도출
        # all_users -> (user_n, emb_size)
        # all_items -> (item_n, emb_size)
        all_users, all_items = self.computer()
        # (batch_size, emb_size)
        users_emb = all_users[users.long()]
        items_emb = all_items
        # self.f = nn.Sigmoid()
        # (batch_size, item_n)
        # 시그모이드 함수 통과시킴 -> 선호할 확률 출력
        rating = self.f(torch.matmul(users_emb, items_emb.t()))
        return rating
    
    def getEmbedding(self, users, pos_items, neg_items):
        # all_users -> (user_n, emb_size)
        # all_items -> (item_n, emb_size)
        all_users, all_items = self.computer()
        users_emb = all_users[users]
        pos_emb = all_items[pos_items]
        neg_emb = all_items[neg_items]
        # 학습 된 가중치 그대로. 원본 그래프와 계산 x
        users_emb_ego = self.embedding_user(users)
        pos_emb_ego = self.embedding_item(pos_items)
        neg_emb_ego = self.embedding_item(neg_items)
        return users_emb, pos_emb, neg_emb, users_emb_ego, pos_emb_ego, neg_emb_ego
    
    # PureMF와 동일한 동작
    def bpr_loss(self, users, pos, neg):
        (users_emb, pos_emb, neg_emb, 
        userEmb0,  posEmb0, negEmb0) = self.getEmbedding(users.long(), pos.long(), neg.long())
        reg_loss = (1/2)*(userEmb0.norm(2).pow(2) + 
                         posEmb0.norm(2).pow(2)  +
                         negEmb0.norm(2).pow(2))/float(len(users))
        pos_scores = torch.mul(users_emb, pos_emb)
        pos_scores = torch.sum(pos_scores, dim=1)
        neg_scores = torch.mul(users_emb, neg_emb)
        neg_scores = torch.sum(neg_scores, dim=1)
        
        loss = torch.mean(torch.nn.functional.softplus(neg_scores - pos_scores))
        
        return loss, reg_loss
       
    def forward(self, users, items):
        # compute embedding
        # all_users -> (user_n, emb_size)
        # all_items -> (item_n, emb_size)
        all_users, all_items = self.computer()
        # print('forward')
        #all_users, all_items = self.computer()
        users_emb = all_users[users]
        items_emb = all_items[items]
        inner_pro = torch.mul(users_emb, items_emb)
        gamma     = torch.sum(inner_pro, dim=1)
        return gamma
